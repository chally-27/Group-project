export function getHerbById(id: string): Herb | undefined {
  return herbs.find((h) => h.id === id);
}

export function searchHerbs(query: string): Herb[] {
  const q = query.toLowerCase();
  return herbs.filter(
    (h) =>
      h.nameSwahili.toLowerCase().includes(q) ||
      h.nameEnglish.toLowerCase().includes(q) ||
      h.scientificName.toLowerCase().includes(q),
  );
}
